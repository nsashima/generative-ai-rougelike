// Colors mapping helper
export function getItemColors(type: ItemType, itemColor: string): { [key: number]: string } {
  if (type === 'gold') {
    return {
      1: '#b45309', // Outline / shadow
      2: '#facc15', // Base Gold Yellow
      3: '#fef08a'  // Top-right metallic highlight
    };
  } else if (type === 'potion_heal' || type === 'potion_strength') {
    return {
      1: '#e2e8f0', // Glass border
      2: itemColor, // Red (heal) or Blue (strength)
      3: '#ffffff'  // Shine highlight
    };
  } else if (
    type === 'scroll_teleport' ||
    type === 'scroll_fireball' ||
    type === 'scroll_sleep' ||
    type === 'scroll_thunder' ||
    type === 'scroll_repair' ||
    type === 'scroll_drain'
  ) {
    const scrollColors: { [key: string]: { mark: string; ribbon: string } } = {
      'scroll_teleport': { mark: '#8b5cf6', ribbon: '#3b82f6' },
      'scroll_fireball': { mark: '#f97316', ribbon: '#ef4444' },
      'scroll_sleep': { mark: '#38bdf8', ribbon: '#0ea5e9' },
      'scroll_thunder': { mark: '#facc15', ribbon: '#eab308' },
      'scroll_repair': { mark: '#4ade80', ribbon: '#16a34a' },
      'scroll_drain': { mark: '#f472b6', ribbon: '#db2777' }
    };
    const config = scrollColors[type] || { mark: '#8b5cf6', ribbon: '#3b82f6' };
    return {
      1: '#b45309', // Wooden end
      2: '#fef3c7', // Aged paper
      3: config.mark, // Custom markings
      4: config.ribbon  // Custom ribbon
    };
  } else if (type === 'weapon_sword') {
    return {
      1: '#cbd5e1', // Metal blade
      2: '#ffffff', // Shine
      3: '#fbbf24', // Golden crossguard
      4: '#78350f'  // Leather hilt
    };
  } else if (type === 'armor_shield') {
    return {
      1: '#94a3b8', // Steel rim
      2: '#0ea5e9', // Blue field
      3: '#fbbf24'  // Golden crown/emblem
    };
  } else if (
    type === 'ring_attack' ||
    type === 'ring_defense' ||
    type === 'ring_durability' ||
    type === 'ring_reflect' ||
    type === 'ring_heal'
  ) {
    return {
      1: '#78350f', // Dark metal outline
      2: '#fbbf24', // Shining gold base
      3: itemColor || '#facc15', // Gem color
      4: '#ffffff'  // Gem shine (white)
    };
  }
  return {};
}
